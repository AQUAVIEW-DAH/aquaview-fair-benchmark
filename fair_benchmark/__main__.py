"""CLI entry point: python -m fair_benchmark"""

from __future__ import annotations

import argparse
import asyncio
import logging
import sys
from datetime import date
from pathlib import Path

from rich.console import Console
from rich.table import Table

from . import __version__
from .checks import check_accessible, check_findable, check_interoperable, check_reusable
from .delta import compute_delta, find_previous_report, format_delta_markdown, load_previous
from .reporter import generate_json, generate_markdown, save_reports
from .sampler import collect_sample
from .scorer import score_findings

console = Console()
DEFAULT_REPORTS_DIR = Path("reports")


def main() -> None:
    parser = argparse.ArgumentParser(
        prog="fair-benchmark",
        description="AQUAVIEW FAIR Data Principles Benchmark",
    )
    parser.add_argument(
        "-o", "--output-dir",
        default=str(DEFAULT_REPORTS_DIR),
        help="Directory for reports (default: ./reports)",
    )
    parser.add_argument(
        "--stac-url",
        default=None,
        help="Override STAC API URL (default: env AQUAVIEW_STAC_URL or production)",
    )
    parser.add_argument(
        "--json-only",
        action="store_true",
        help="Output JSON to stdout instead of saving files",
    )
    parser.add_argument(
        "-v", "--verbose",
        action="store_true",
        help="Enable debug logging",
    )
    parser.add_argument(
        "--version",
        action="version",
        version=f"%(prog)s {__version__}",
    )
    args = parser.parse_args()

    if args.verbose:
        logging.basicConfig(level=logging.DEBUG, format="%(levelname)s %(name)s: %(message)s")
    else:
        logging.basicConfig(level=logging.WARNING)

    if args.stac_url:
        import fair_benchmark.config as config
        config.STAC_API_URL = args.stac_url

    asyncio.run(_run(args))


async def _run(args: argparse.Namespace) -> None:
    run_date = date.today().isoformat()
    output_dir = Path(args.output_dir)

    console.print(f"\n[bold]AQUAVIEW FAIR Benchmark v{__version__}[/bold]")
    console.print(f"Date: {run_date}")
    console.print()

    # Step 0: Check for previous assessment
    prev_path = find_previous_report(output_dir)
    if prev_path:
        console.print(f"Previous assessment found: {prev_path.name}")
    else:
        console.print("Baseline assessment (no prior data)")
    console.print()

    # Step 1: Sample the catalog
    console.print("[bold]Step 1:[/bold] Sampling catalog...", end="")
    sample = await collect_sample()

    if sample.errors:
        console.print(f" [yellow]{len(sample.errors)} warnings[/yellow]")
        for err in sample.errors:
            console.print(f"  [yellow]! {err}[/yellow]")
    else:
        console.print(" [green]OK[/green]")

    console.print(f"  Collections: {len(sample.collections)}")
    console.print(f"  Total items indexed: {sample.total_item_count or 'unknown'}")
    console.print(f"  Items sampled: {len(sample.all_items)}")
    console.print()

    # Step 2: Run all checks
    console.print("[bold]Step 2:[/bold] Evaluating FAIR principles...")
    findings = []
    findings.extend(check_findable(sample))
    findings.extend(check_accessible(sample))
    findings.extend(check_interoperable(sample))
    findings.extend(check_reusable(sample))
    console.print(f"  Total findings: {len(findings)}")
    console.print()

    # Step 3: Score
    console.print("[bold]Step 3:[/bold] Computing scores...")
    scorecard = score_findings(findings, sample)
    console.print()

    # Display platform scorecard
    _print_platform_scorecard(scorecard)
    _print_source_leaderboard(scorecard)

    # Step 4: Save or output
    if args.json_only:
        import json
        report = generate_json(scorecard, run_date)
        console.print(json.dumps(report, indent=2))
    else:
        md_path, json_path = save_reports(scorecard, output_dir, run_date)

        # Append delta section to markdown if we have a previous report
        if prev_path:
            prev_data = load_previous(prev_path)
            curr_data = generate_json(scorecard, run_date)
            delta = compute_delta(curr_data, prev_data)
            delta_md = format_delta_markdown(delta)

            md_content = md_path.read_text()
            # Insert delta before the footer
            md_content = md_content.replace(
                "---\n*Generated by",
                f"{delta_md}\n---\n*Generated by",
            )
            md_path.write_text(md_content)

            change = delta["overall"]["change"]
            arrow = "^" if change > 0 else "v" if change < 0 else "="
            console.print(
                f"  Delta: {delta['overall']['previous_grade']} -> "
                f"{delta['overall']['current_grade']} ({change:+.2f}) {arrow}"
            )

        console.print(f"\n[bold]Reports saved:[/bold]")
        console.print(f"  Markdown: {md_path}")
        console.print(f"  JSON:     {json_path}")

    console.print()


def _print_platform_scorecard(scorecard) -> None:
    table = Table(title=f"Platform FAIR Scorecard: {scorecard.overall_grade} "
                  f"({scorecard.weighted_numeric:.2f}/4.00)")
    table.add_column("Principle", style="bold")
    table.add_column("Grade", justify="center")
    table.add_column("Score", justify="right")
    table.add_column("Weight", justify="right")
    table.add_column("Gap", max_width=50)

    for pid in ["F1", "F2", "F3", "F4", "A1", "A1.1", "A1.2", "A2",
                "I1", "I2", "I3", "R1", "R1.1", "R1.2", "R1.3"]:
        ps = scorecard.principles.get(pid)
        if ps:
            grade_style = {
                "A": "green", "B": "blue", "C": "yellow", "D": "red", "F": "bold red",
            }.get(ps.grade, "")
            table.add_row(
                pid,
                f"[{grade_style}]{ps.grade}[/{grade_style}]",
                f"{ps.numeric:.2f}",
                f"{ps.weight}x",
                ps.gap_summary[:50] if ps.gap_summary else "",
            )

    console.print(table)
    console.print()


def _print_source_leaderboard(scorecard) -> None:
    sorted_sources = sorted(
        scorecard.source_scorecards.values(),
        key=lambda s: s.weighted_numeric,
        reverse=True,
    )

    if not sorted_sources:
        return

    table = Table(title="Per-Source FAIR Scores")
    table.add_column("#", justify="right", style="dim")
    table.add_column("Source", style="bold")
    table.add_column("Items", justify="right")
    table.add_column("Grade", justify="center")
    table.add_column("Score", justify="right")

    for i, sc in enumerate(sorted_sources, 1):
        grade_style = {
            "A": "green", "B": "blue", "C": "yellow", "D": "red", "F": "bold red",
        }.get(sc.overall_grade, "")
        table.add_row(
            str(i),
            f"{sc.collection_title}",
            f"{sc.item_count:,}",
            f"[{grade_style}]{sc.overall_grade}[/{grade_style}]",
            f"{sc.weighted_numeric:.2f}",
        )

    console.print(table)
    console.print()


if __name__ == "__main__":
    main()
