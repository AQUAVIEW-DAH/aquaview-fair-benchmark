"""Report generation: Markdown + JSON output."""

from __future__ import annotations

import json
from datetime import date
from pathlib import Path
from typing import Any

from .models import PlatformScorecard, SourceScorecard


def generate_markdown(scorecard: PlatformScorecard, run_date: str) -> str:
    """Generate a complete Markdown FAIR assessment report."""
    lines: list[str] = []

    lines.append("# AQUAVIEW FAIR Data Principles Assessment")
    lines.append(f"**Date:** {run_date}")
    lines.append("**Assessor:** aquaview-fair-benchmark (automated)")
    lines.append(f"**Catalog:** {scorecard.total_collections} collections, "
                 f"{scorecard.total_items:,} items")
    lines.append("")

    # Executive summary
    lines.append("## Executive Summary")
    lines.append("")
    lines.append(f"Overall platform FAIR grade: **{scorecard.overall_grade}** "
                 f"({scorecard.weighted_numeric:.2f}/4.00)")
    lines.append("")

    # Per-source leaderboard
    lines.append("## Source Leaderboard")
    lines.append("")
    sorted_sources = sorted(
        scorecard.source_scorecards.values(),
        key=lambda s: s.weighted_numeric,
        reverse=True,
    )
    lines.append("| Rank | Source | Items | Grade | Score |")
    lines.append("|------|--------|-------|-------|-------|")
    for i, sc in enumerate(sorted_sources, 1):
        lines.append(
            f"| {i} | {sc.collection_title} ({sc.collection_id}) | "
            f"{sc.item_count:,} | {sc.overall_grade} | {sc.weighted_numeric:.2f} |"
        )
    lines.append("")

    # Platform scorecard
    lines.append("## Platform Scorecard")
    lines.append("")
    lines.append("| Principle | Grade | Score | Weight | Findings | Summary |")
    lines.append("|-----------|-------|-------|--------|----------|---------|")
    for pid in ["F1", "F2", "F3", "F4", "A1", "A1.1", "A1.2", "A2",
                "I1", "I2", "I3", "R1", "R1.1", "R1.2", "R1.3"]:
        ps = scorecard.principles.get(pid)
        if ps:
            gap_short = ps.gap_summary[:60] + "..." if len(ps.gap_summary) > 60 else ps.gap_summary
            lines.append(
                f"| {pid} | {ps.grade} | {ps.numeric:.2f} | {ps.weight}x | "
                f"{ps.finding_count} | {gap_short} |"
            )
    lines.append("")

    # Detailed per-source breakdowns
    lines.append("## Per-Source Detail")
    lines.append("")
    for sc in sorted_sources:
        lines.append(f"### {sc.collection_title} ({sc.collection_id})")
        lines.append(f"**Items:** {sc.item_count:,} | **Grade:** {sc.overall_grade} "
                     f"({sc.weighted_numeric:.2f}/4.00)")
        lines.append("")
        lines.append("| Principle | Grade | Score | Gap |")
        lines.append("|-----------|-------|-------|-----|")
        for pid in ["F1", "F2", "F3", "F4", "A1", "A1.1", "A1.2", "A2",
                    "I1", "I2", "I3", "R1", "R1.1", "R1.2", "R1.3"]:
            ps = sc.principles.get(pid)
            if ps:
                gap_short = (ps.gap_summary[:50] + "...") if len(ps.gap_summary) > 50 else ps.gap_summary
                lines.append(f"| {pid} | {ps.grade} | {ps.numeric:.2f} | {gap_short} |")
        lines.append("")

    # Top gaps (across all sources)
    lines.append("## Top Gaps (by impact)")
    lines.append("")
    all_gaps = _collect_gaps(scorecard)
    for i, (principle, gap, remediation, sources) in enumerate(all_gaps[:10], 1):
        lines.append(f"{i}. **{principle}** -- {gap}")
        lines.append(f"   - Affects: {', '.join(sources)}")
        lines.append(f"   - Remediation: {remediation}")
        lines.append("")

    # Remediation roadmap
    lines.append("## Remediation Roadmap")
    lines.append("")
    lines.append("### Quick wins (metadata enrichment)")
    lines.append("")
    for principle, gap, remediation, sources in all_gaps:
        if any(kw in remediation.lower() for kw in ("add", "populate", "include")):
            lines.append(f"- [ ] **{principle}**: {remediation} (affects {len(sources)} sources)")
    lines.append("")
    lines.append("### Medium effort (standards compliance)")
    lines.append("")
    for principle, gap, remediation, sources in all_gaps:
        if any(kw in remediation.lower() for kw in ("map", "declare", "assign", "standard")):
            lines.append(f"- [ ] **{principle}**: {remediation} (affects {len(sources)} sources)")
    lines.append("")
    lines.append("### Larger effort (infrastructure)")
    lines.append("")
    for principle, gap, remediation, sources in all_gaps:
        if any(kw in remediation.lower() for kw in ("policy", "verify", "document", "expose")):
            lines.append(f"- [ ] **{principle}**: {remediation} (affects {len(sources)} sources)")
    lines.append("")

    lines.append("---")
    lines.append("*Generated by [aquaview-fair-benchmark]"
                 "(https://github.com/AQUAVIEW-DAH/aquaview-fair-benchmark)*")

    return "\n".join(lines)


def generate_json(scorecard: PlatformScorecard, run_date: str) -> dict[str, Any]:
    """Generate a machine-readable JSON report."""
    return {
        "date": run_date,
        "overall_grade": scorecard.overall_grade,
        "overall_score": round(scorecard.weighted_numeric, 2),
        "total_items": scorecard.total_items,
        "total_collections": scorecard.total_collections,
        "principles": {
            pid: {
                "grade": ps.grade,
                "score": ps.numeric,
                "weight": ps.weight,
                "finding_count": ps.finding_count,
                "gap": ps.gap_summary,
                "remediation": ps.remediation,
            }
            for pid, ps in scorecard.principles.items()
        },
        "sources": {
            coll_id: {
                "title": sc.collection_title,
                "item_count": sc.item_count,
                "overall_grade": sc.overall_grade,
                "overall_score": round(sc.weighted_numeric, 2),
                "principles": {
                    pid: {
                        "grade": ps.grade,
                        "score": ps.numeric,
                    }
                    for pid, ps in sc.principles.items()
                },
            }
            for coll_id, sc in scorecard.source_scorecards.items()
        },
    }


def save_reports(
    scorecard: PlatformScorecard,
    output_dir: str | Path,
    run_date: str | None = None,
) -> tuple[Path, Path]:
    """Save Markdown and JSON reports. Returns (md_path, json_path)."""
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    if not run_date:
        run_date = date.today().isoformat()

    # Check for existing file, append sequence number if needed
    md_name = f"AQUAVIEW_FAIR_Assessment_{run_date}.md"
    json_name = f"AQUAVIEW_FAIR_Assessment_{run_date}.json"

    md_path = output_dir / md_name
    json_path = output_dir / json_name

    seq = 2
    while md_path.exists():
        md_path = output_dir / f"AQUAVIEW_FAIR_Assessment_{run_date}_{seq}.md"
        json_path = output_dir / f"AQUAVIEW_FAIR_Assessment_{run_date}_{seq}.json"
        seq += 1

    md_content = generate_markdown(scorecard, run_date)
    md_path.write_text(md_content)

    json_content = generate_json(scorecard, run_date)
    json_path.write_text(json.dumps(json_content, indent=2))

    return md_path, json_path


def _collect_gaps(
    scorecard: PlatformScorecard,
) -> list[tuple[str, str, str, list[str]]]:
    """Collect and deduplicate gaps across all sources, sorted by frequency."""
    gap_map: dict[tuple[str, str], tuple[str, list[str]]] = {}

    for coll_id, sc in scorecard.source_scorecards.items():
        for pid, ps in sc.principles.items():
            if ps.gap_summary:
                key = (pid, ps.gap_summary)
                if key not in gap_map:
                    gap_map[key] = (ps.remediation, [])
                gap_map[key][1].append(coll_id)

    # Sort by number of affected sources (descending), then principle
    results = [
        (pid, gap, remediation, sources)
        for (pid, gap), (remediation, sources) in gap_map.items()
    ]
    results.sort(key=lambda x: (-len(x[3]), x[0]))
    return results
